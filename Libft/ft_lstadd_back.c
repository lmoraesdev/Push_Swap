#include "libft.h"

void ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list *last_element;

	if (!lst || !new)
		return;
	if (*lst == NULL)
		*lst = new;
	else
	{
		last_element = ft_lstlast(*lst);
		last_element->next = new;
	}
}
